create table if not exists public.users (
  "Id" uuid primary key,
  "Username" varchar(100) not null unique,
  "PasswordHash" text not null,
  "CreatedAt" timestamptz not null,
  "IngestionToken" varchar(64) not null unique
);

create table if not exists public.servers (
  "Id" varchar(200) primary key,
  "HostName" varchar(255) not null,
  "IpAddress" varchar(45),
  "LastHeartbeatAt" timestamptz not null,
  "OwnerUserId" uuid references public.users ("Id") on delete set null
);

create index if not exists "IX_servers_OwnerUserId"
  on public.servers ("OwnerUserId");

create table if not exists public.performance_metrics (
  "Id" bigint generated by default as identity primary key,
  "ServerId" varchar(200) not null
    references public.servers ("Id") on delete cascade,
  "Timestamp" timestamptz not null,
  "CpuUsagePct" real not null,
  "MemoryUsedBytes" bigint not null,
  "MemoryTotalBytes" bigint not null,
  "DiskUtilizationPct" real not null,
  "NetworkInBytesSec" bigint not null
);

create index if not exists "IX_performance_metrics_ServerId_Timestamp"
  on public.performance_metrics ("ServerId", "Timestamp" desc);

create table if not exists public.tags (
  "Name" varchar(50) primary key
);

create table if not exists public.server_tags (
  "ServerId" varchar(200) not null
    references public.servers ("Id") on delete cascade,
  "TagName" varchar(50) not null
    references public.tags ("Name") on delete cascade,
  primary key ("ServerId", "TagName")
);

create index if not exists "IX_server_tags_TagName"
  on public.server_tags ("TagName");

drop function if exists public.netrascope_ingest_metric(
  text, timestamptz, real, bigint, bigint, real, bigint, text, uuid
);

create function public.netrascope_ingest_metric(
  p_server_id text,
  p_timestamp timestamptz,
  p_cpu_usage_pct real,
  p_memory_used_bytes bigint,
  p_memory_total_bytes bigint,
  p_disk_utilization_pct real,
  p_network_in_bytes_sec bigint,
  p_ip_address text,
  p_owner_user_id uuid
)
returns boolean
language plpgsql
set search_path = ''
as $$
begin
  insert into public.servers (
    "Id", "HostName", "IpAddress", "LastHeartbeatAt", "OwnerUserId"
  )
  values (
    p_server_id, p_server_id, p_ip_address, now(), p_owner_user_id
  )
  on conflict ("Id") do update set
    "HostName" = excluded."HostName",
    "IpAddress" = excluded."IpAddress",
    "LastHeartbeatAt" = excluded."LastHeartbeatAt",
    "OwnerUserId" = excluded."OwnerUserId"
  where public.servers."OwnerUserId" is null
     or public.servers."OwnerUserId" = p_owner_user_id;

  if not found then
    return false;
  end if;

  insert into public.performance_metrics (
    "ServerId",
    "Timestamp",
    "CpuUsagePct",
    "MemoryUsedBytes",
    "MemoryTotalBytes",
    "DiskUtilizationPct",
    "NetworkInBytesSec"
  )
  values (
    p_server_id,
    p_timestamp,
    p_cpu_usage_pct,
    p_memory_used_bytes,
    p_memory_total_bytes,
    p_disk_utilization_pct,
    p_network_in_bytes_sec
  );

  return true;
end;
$$;

create or replace function public.netrascope_replace_server_tags(
  p_server_id text,
  p_owner_user_id uuid,
  p_tags text[]
)
returns boolean
language plpgsql
set search_path = ''
as $$
begin
  if not exists (
    select 1
    from public.servers
    where "Id" = p_server_id and "OwnerUserId" = p_owner_user_id
  ) then
    return false;
  end if;

  delete from public.server_tags where "ServerId" = p_server_id;

  insert into public.tags ("Name")
  select distinct tag_name
  from unnest(p_tags) as input(tag_name)
  on conflict ("Name") do nothing;

  insert into public.server_tags ("ServerId", "TagName")
  select p_server_id, tag_name
  from unnest(p_tags) as input(tag_name);

  return true;
end;
$$;

